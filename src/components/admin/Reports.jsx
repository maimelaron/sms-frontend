const monthStart = () => { const d = new Date(); d.setDate(1); return d.toISOString().split('T')[0]; };
const todayStr  = () => new Date().toISOString().split('T')[0];

const exportCSV = (rows, filename) => {
    if (!rows.length) return;
    const keys = Object.keys(rows[0]);
    const lines = [
        keys.join(','),
        ...rows.map(r => keys.map(k => {
            const v = String(r[k] ?? '').replace(/"/g, '""');
            return /[,"\n\r]/.test(v) ? `"${v}"` : v;
        }).join(','))
    ];
    const blob = new Blob([lines.join('\r\n')], { type: 'text/csv;charset=utf-8;' });
    const a = Object.assign(document.createElement('a'), { href: URL.createObjectURL(blob), download: filename });
    document.body.appendChild(a); a.click(); document.body.removeChild(a);
    URL.revokeObjectURL(a.href);
};

const fmtDate = (v) => {
    if (!v) return '—';
    try { return new Date(v?._seconds ? v._seconds * 1000 : v).toLocaleDateString('en-ZA'); }
    catch { return '—'; }
};

const fmtDateTime = (v) => {
    if (!v) return '—';
    try { return new Date(v?._seconds ? v._seconds * 1000 : v).toLocaleString('en-ZA'); }
    catch { return '—'; }
};

const inRange = (dateVal, from, to) => {
    if (!dateVal) return true;
    try {
        const d = new Date(dateVal?._seconds ? dateVal._seconds * 1000 : dateVal);
        if (isNaN(d)) return true;
        if (from && d < new Date(from)) return false;
        if (to   && d > new Date(to + 'T23:59:59')) return false;
        return true;
    } catch { return true; }
};

