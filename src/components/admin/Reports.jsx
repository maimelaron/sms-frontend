        const tripPayments = allPayments.filter(p => p.tripId === t.tripId || p.tripId === String(t.tripId));
        const paid = tripPayments.filter(p => p.status === 'COMPLETED' || p.status === 'PAID').length;
        const revenue = paid * (t.price || 0);
        return { ...t, registrations: tripPayments.length, paidCount: paid, revenue };
